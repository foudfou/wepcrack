#define WEP_AUTH_FRAME_HDR 0x01,0x00,0x03,0x00,0x00,0x00,0x10,0x80

/* The encryption process is as follows:
 * - create a new 802.11 MAC frame header with the correct transaction sequence
 *   number (0x0003 here 0x03,0x00),
 * - append the challenge text,
 * - compute the CRC32 (ICV) of the result and append it,
 * - finally RC4-encrypt the result.
 *
 * So it's important to understand that the input data does not only comprise
 * the challenge text! And this is why it is usually said that the result of
 * the encryption is as long as the input data + 4 bytes for the ICV.
 *
 * Note that wireshark analyses the ICV as separate from the data.
 *
 * Interesting sources of information include:
 * - 802.11 Wireless Networks The Definitive Guide (ISBN 0-596-10052-3)
 * - ANSI/IEEE Std 802.11, 1999 Edition (ISO/IEC 8802-11:1999(E))
 */

